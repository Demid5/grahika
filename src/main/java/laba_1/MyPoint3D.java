package laba_1;

public class MyPoint3D {
    private int x, y, z;

    public MyPoint3D() {

    }

    public MyPoint3D(int x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}
