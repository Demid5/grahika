package laba_1;

public class MyPoint3D {
    private double x, y, z;

    public MyPoint3D() {

    }

    public MyPoint3D(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }
}
